package com.cc.entity;

public class Company {
	
	private Integer cid;
	private String cname;
	private String clianxi;
	private String cphone;
	private String caddress;
	private String cbeijing;
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getClianxi() {
		return clianxi;
	}
	public void setClianxi(String clianxi) {
		this.clianxi = clianxi;
	}
	public String getCphone() {
		return cphone;
	}
	public void setCphone(String cphone) {
		this.cphone = cphone;
	}
	public String getCaddress() {
		return caddress;
	}
	public void setCaddress(String caddress) {
		this.caddress = caddress;
	}
	public String getCbeijing() {
		return cbeijing;
	}
	public void setCbeijing(String cbeijing) {
		this.cbeijing = cbeijing;
	}
	
	
	
}
